use scalar::*;

fn main() {
    //sum
    println!("Sum of 5 and 6 equals: {}", sum(5, 6));

    //diff
    println!("Diff of 10 and 2 equals: {}", diff(10, 2));

    //pro
    println!("Pro of 3 and 3 equals: {}", pro(3, 3));

    //quo
    println!("Quo of 15 and 3 equals: {}", quo(15, 3));

    //rem
    println!("Rem of 11 and 3 equals: {}", rem(11, 3));
}
